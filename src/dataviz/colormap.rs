/// Map t ∈ [0.0, 1.0] to a viridis [`crate::Color`].
///
/// - t = 0.0 → dark purple (VIRIDIS_LUT[0])
/// - t = 1.0 → bright yellow (VIRIDIS_LUT[255])
/// - Values outside [0, 1] are clamped before lookup.
pub fn viridis_color(t: f64) -> crate::Color {
    let t = t.clamp(0.0, 1.0);
    let idx = (t * 255.0).round() as usize;
    let idx = idx.min(255);
    let (r, g, b) = VIRIDIS_LUT[idx];
    crate::Color::rgb(
        (r * 255.0).round() as u8,
        (g * 255.0).round() as u8,
        (b * 255.0).round() as u8,
    )
}

#[cfg(test)]
mod tests {
    use super::*;

    /// viridis index 0 is deep purple: low red, dominant blue channel.
    /// Canonical value: (0.267004, 0.004874, 0.329415) → u8 (68, 1, 84).
    /// b=84 > 50, r=68 < 100, and b is the dominant non-green channel.
    #[test]
    fn viridis_at_zero_is_purple() {
        let c = viridis_color(0.0);
        // Deep purple: red is low, blue is dominant over green
        assert!(
            c.r < 100,
            "t=0.0 should have low red (purple); got r={}",
            c.r
        );
        assert!(
            c.b > c.g,
            "t=0.0 should have blue > green (purple hue); got b={}, g={}",
            c.b,
            c.g
        );
        assert!(
            c.b > 50,
            "t=0.0 should have meaningful blue component (purple); got b={}",
            c.b
        );
    }

    #[test]
    fn viridis_at_one_is_yellow() {
        let c = viridis_color(1.0);
        // Bright yellow: high red, high green, low blue
        // Canonical endpoint (0.993248, 0.906157, 0.143936) → u8 (253, 231, 37)
        assert!(
            c.r > 200,
            "t=1.0 should have high red (yellow); got r={}",
            c.r
        );
        assert!(
            c.g > 200,
            "t=1.0 should have high green (yellow); got g={}",
            c.g
        );
        assert!(
            c.b < 100,
            "t=1.0 should have low blue (yellow); got b={}",
            c.b
        );
    }

    #[test]
    fn viridis_clamps_below_zero() {
        let below = viridis_color(-1.0);
        let zero = viridis_color(0.0);
        assert_eq!(
            below, zero,
            "viridis_color(-1.0) should equal viridis_color(0.0)"
        );
    }

    #[test]
    fn viridis_clamps_above_one() {
        let above = viridis_color(2.0);
        let one = viridis_color(1.0);
        assert_eq!(
            above, one,
            "viridis_color(2.0) should equal viridis_color(1.0)"
        );
    }

    #[test]
    fn viridis_midpoint_is_teal_green() {
        // viridis(0.5) → index 128 → (0.127568, 0.566949, 0.550556) → u8 (33, 145, 140)
        // teal-green: green channel dominates over red
        let c = viridis_color(0.5);
        assert!(
            c.g > c.r,
            "viridis midpoint should be teal-green (g > r); got r={}, g={}, b={}",
            c.r,
            c.g,
            c.b
        );
    }
}
